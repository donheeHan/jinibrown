package jiniShop.vo;

public class CartVO {
	private String c_no;
	private String c_user;
	private int c_p_no;
	
	public String getC_no() {
		return c_no;
	}
	public void setC_no(String c_no) {
		this.c_no = c_no;
	}
	public String getC_user() {
		return c_user;
	}
	public void setC_user(String c_user) {
		this.c_user = c_user;
	}
	public int getC_p_no() {
		return c_p_no;
	}
	public void setC_p_no(int c_p_no) {
		this.c_p_no = c_p_no;
	}
	
	
}
